set_a = {"Violet","Indigo","Blue","Purple"}
set_b = {"Red","Orange","Yellow","Green","Blue","Purple"}

print("Original Sets are:")
print(set_a)
print(set_b)

print("The Intersection of the two sets are:")
set_c = set_a.intersection(set_b)
print(set_c)

print("The Union of the two sets are:")
set_1 = set_b.union(set_a)
print(set_1)

print("The difference of the two sets are:")
set_2 = set_a.difference(set_b)
print(set_2)