print("This program will check if the number is greater or smaller than 15.")
num = int(input("Enter the number: "))
if num >= 15:
    print("The number is greater than 15.")
    print("You are in the if block.")
else:
    print("The number is smaller than 15.")
    print("You are in the else block.")

print("You are out of the if-else block.")
